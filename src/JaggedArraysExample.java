
public class JaggedArraysExample {
    public static void main(String[] args) {

        int[][] array = new int[5][];
        array [0] = new int[1];
        array [1] = new int[3];
        array [2] = new int[5];
        array [3] = new int[1];
        array [4] = new int[1];
        System.out.println(array[2][1]);
    }
}
